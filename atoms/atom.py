class Atom:
    def __init__(self, symbol, name, atomic_number, mass, electrons, color):
        self.symbol = symbol
        self.name = name
        self.atomic_number = atomic_number
        self.mass = mass
        self.electrons = electrons
        self.color = color

        self.x = 0
        self.y = 0
        self.width = 0
        self.height = 0
